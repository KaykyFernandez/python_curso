#tipos de float
# int -> numero inteiro
# o tipo int representa qualquer numero inteiro
# positivo ou negativo. int sem sinal é considerado positivo.
print(11) # int
print(-11) #int
print(0) # int
# float -> numero flutuante
# o tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo
print(1.1, 10.10)# float
print(0.0, -1.5) # float


# funcao type mostra o tipo que o python inferiu o valor.
print(  type('kayky') )
print(  type(6) )
print(  type(60.0) )

